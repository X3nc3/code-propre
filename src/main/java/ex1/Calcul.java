package ex1;
/** classe qui effectue des action de calcul sur des nombres*/
public class Calcul {

	/** Retourne l'addition entre 2 entiers
	 * @param a entier 1
	 * @param b entier 2
	 * @return int
	 */
	public static int addition(int a, int b){
		return a+b;
	}

	/** Retourne la soustraction entre 2 entiers
	 * @param a entier 1
	 * @param b entier 2
	 * @return int
	 */
	public static int soustraction(int a, int b){
		return a-b;
	}
}
